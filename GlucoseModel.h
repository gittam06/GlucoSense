double predictGlucoseSVR(double ratio, double variability) {
    double input[2] = {ratio, variability};
    double scaled[2];
    for (int j = 0; j < 2; j++) {
        scaled[j] = (input[j] - FEAT_MEAN[j]) / FEAT_STD[j];
    }
    double result = INTERCEPT;
    for (int i = 0; i < N_SUPPORT; i++) {
        double dot = scaled[0]*SUPPORT_VECTORS[i][0] + scaled[1]*SUPPORT_VECTORS[i][1];
        double kernel_val = GAMMA * dot + COEF0;
        double poly_val = 1.0;
        for (int d = 0; d < DEGREE; d++) poly_val *= kernel_val;
        result += DUAL_COEFS[i] * poly_val;
    }
    if (result < 40.0) result = 40.0;
    if (result > 400.0) result = 400.0;
    return result;
}

